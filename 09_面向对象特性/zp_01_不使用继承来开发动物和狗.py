class Animal:
    def eat(self):
        print("吃")

    def drink(self):
        print("喝")

    def run(self):
        print("跑")

    def sleep(self):
        print("睡")

class Dog:
    def eat(self):
        print("吃")

    def drink(self):
        print("喝")

    def run(self):
        print("跑")

    def sleep(self):
        print("睡")

    def brak(self):
        print("汪汪叫")

# 创建一个对象
wangcai = Dog()
wangcai.run()
wangcai.eat()
wangcai.drink()
wangcai.sleep()
wangcai.brak()